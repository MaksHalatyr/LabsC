#include <stdio.h>

int main() {
    int m, n; // розміри таблиці
    printf("Введіть кількість рядків m: ");
    scanf("%d", &m);
    printf("Введіть кількість стовпців n: ");
    scanf("%d", &n);

    // Перевірка на невірний ввід (наприклад, m або n менше або дорівнює 0)
    if (m <= 0 || n <= 0) {
        printf("Невірний ввід для розмірів таблиці.\n");
        return 1; // Вихід з програми з помилкою
    }

    int k, l; // номер рядка та стовпця для обміну
    printf("Введіть номер рядка k: ");
    scanf("%d", &k);
    printf("Введіть номер стовпчика l: ");
    scanf("%d", &l);

    // Перевірка на коректність введених номерів рядка та стовпчика
    if (k < 1 || k > m || l < 1 || l > n) {
        printf("Невірний ввід для номерів рядка та стовпчика.\n");
        return 1; // Вихід з програми з помилкою
    }

    // Оголошення та ініціалізація таблиці
    int table[m][n];
    printf("Введіть елементи таблиці %dx%d:\n", m, n);
    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            scanf("%d", &table[i][j]);
        }
    }

    // Обмін місцями елементів k-го рядка та l-го стовпчика
    for (int i = 0; i < m; ++i) {
        int temp = table[i][k - 1];
        table[i][k - 1] = table[l - 1][i];
        table[l - 1][i] = temp;
    }

    // Виведення результату
    printf("Результат обміну місцями:\n");
    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            printf("%d ", table[i][j]);
        }
        printf("\n");
    }

    return 0;
}
