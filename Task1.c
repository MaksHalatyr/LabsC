#include <stdio.h>

int main() {
    // Введення даних
    double V1, V2, A, T;
    printf("Введіть швидкість першого автомобіля (км/год): ");
    scanf("%lf", &V1);

    printf("Введіть швидкість другого автомобіля (км/год): ");
    scanf("%lf", &V2);

    printf("Введіть прискорення (км/год^2): ");
    scanf("%lf", &A);

    printf("Введіть час (години): ");
    scanf("%lf", &T);

    // Переведення швидкостей з км/год в км/с
    V1 /= 3600;
    V2 /= 3600;

    // Обчислення відстані між автомобілями
    double S = (V1 + V2) * T + 0.5 * A * T * T;

    // Виведення результату
    printf("Відстань між автомобілями через %.2f годин: %.2f км\n", T, S);

    return 0;
}
